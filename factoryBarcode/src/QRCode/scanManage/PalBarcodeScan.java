    private void setModel(String barCode) {
        Stock stock = DataBaseUtil.findStockByBarCode(barCode);
        if (stock != null) {
            txtProdName.setText(stock.getName());
            txtName2.setText(stock.getName2());
            txtProdCode.setText(stock.getBarcode());
            remark.setText(stock.getRemark());
            txtProdNum.setText("1");
            txtInPrice.setText(stock.getInPrice().toString());
            txtRemainShare.setText(stock.getRemainshare().toString());
            id = stock.getId();
        } else {
            id = 0;
            resetPage();
            MessageUtils.showErrorMessage("未找到该商品信息，请快速录入商品或在新增功能中录入商品！", this);
        }
    }

    void resetPage() {
        txtProdName.setText(null);
        txtName2.setText(null);
        remark.setText(null);
        txtProdNum.setText(null);
        txtInPrice.setText(null);
        txtRemainShare.setText(null);
    }

    private void reQueryPage() {
           Stock stock = DataBaseUtil.findStockByBarCode(txtProdCode.getText());
        if (stock != null) {
            txtProdName.setText(stock.getName());
            txtName2.setText(stock.getName2());
            txtProdCode.setText(stock.getBarcode());
            remark.setText(stock.getRemark());
            txtProdNum.setText("1");
            txtInPrice.setText(stock.getInPrice().toString());
            txtRemainShare.setText(stock.getRemainshare().toString());
            id = stock.getId();
        }
    }
