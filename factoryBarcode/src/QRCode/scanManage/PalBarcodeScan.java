    private int saveOrUpdate(Stock stock) {
        int i = 0;
        if (stock.getId() != null) {
            long number = Long.valueOf(txtProdNum.getText());
            stock.setRemainshare(stock.getRemainshare() + number);
            i = DataBaseUtil.updateStock(stock);
        } else {
            stock.setRemainshare(1l);
            i = DataBaseUtil.saveStock(stock);
        }
       reQueryPage();
        return i;
    }

    private Stock getModel() {
        Stock stock = new Stock();
        if (id != 0) {
            stock.setId(id);
        }
        stock.setBarcode(txtProdCode.getText());
        stock.setName2(txtName2.getText());
        stock.setName(txtProdName.getText());
        stock.setInPrice(new BigDecimal(txtInPrice.getText()));
        stock.setRemainshare(Long.valueOf(txtRemainShare.getText()));
        stock.setRemark(remark.getText());
        return stock;
    }
