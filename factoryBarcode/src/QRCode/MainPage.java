    private void showtabPanel(String title) {
        Component c = null;
        if (compMap.get(title) != null) {
            c = compMap.get(title);
        } else {
            if (title.equals("端口扫码管理")) {
                c = new SocketScanManage();
            } else if (title.equals("产品管理")) {
                c = new PalProductManage();
            } else if (title.equals("任务管理")) {
                c = new PalTaskManage();
            } else if (title.equals("公司管理")) {
                c = new PalCompanyManage();
            } else if (title.equals("商品管理")) {
                c = new PalStockManage();
            } else if (title.equals("柜台交易")) {
                c = new PalDealPage();
            } else if (title.equals("商品快查")) {
                c = new PalQuickScan();
            }
            compMap.put(title, c);
        }
        jTabbedPane1.add(title, c);
        ButtonTabComponent btn = new ButtonTabComponent(jTabbedPane1, compMap, title);
        jTabbedPane1.setTabComponentAt(jTabbedPane1.indexOfComponent(c), btn);
        jTabbedPane1.setSelectedComponent(c);
        initCompData(c);
    }

