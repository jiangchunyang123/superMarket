    //替代传统的frame.setSize(width,height);

    public static void setSize(JFrame frame, int width, int height) {
        //一定要在JFrame显示之前调用
        frame.pack();

        //在默认的setResizable(true); 情况下,titleDefaultHeight = 27,但如果 setResizable(false); titleDefaultHeight = 25
        int titleDefaultHeight = frame.isResizable() ? 27 : 25; //默认开发阶段使用windows 经典主题,标题栏高度= 27 

        int containerHeight = frame.getContentPane().getHeight();
        //System.out.println("containerHeight:" + containerHeight); //windows 经典主题或xp主题下 = 1
        //int rootPaneHeight = frame.getRootPane().getHeight(); //取得的高度和container的高度是一样的

        int frameHeight = frame.getHeight();
        //System.out.println("frameHeight:" + frameHeight); //windows经典主题下= 28, windows xp 主题下 = 33  

        int titleHeight = frameHeight - containerHeight; //这里就可以得到当前标题栏的实际高度
        if (titleHeight > titleDefaultHeight) {
            int h = titleHeight - titleDefaultHeight; //得到当前使用的主题,标题栏 - 经典主题下的标题栏高度 = 差值     
            frame.setSize(width, height + h); //最终确定窗口的高度
        } else {
            frame.setSize(width, height);
        }
    }

    public static void initGlobalFontSetting(Font fnt) {
        FontUIResource fontRes = new FontUIResource(fnt);
        for (Enumeration keys = UIManager.getDefaults().keys(); keys.hasMoreElements();) {
            Object key = keys.nextElement();
            Object value = UIManager.get(key);
            if (value instanceof FontUIResource) {
                UIManager.put(key, fontRes);
            }
        }

    }

